    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        try {
        int itemnum = Integer.parseInt(jTextField1.getText());

        con = DriverManager.getConnection(host, uName, uPass);
        stmt = con.createStatement();


        String SQL = "SELECT * FROM HANZ.HANZTABLE WHERE ITEMNUM = " + itemnum +"";

        ResultSet rs = stmt.executeQuery(SQL);

        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();


        tableModel.setRowCount(0);

        while (rs.next()) {
            int retrievedItemNum = rs.getInt("ITEMNUM");
            String retrievedItemName = rs.getString("ITEMNAME");
            String retrievedItemDesc = rs.getString("ITEMDESCRIP");
            int retrievedItemQuantity = rs.getInt("ITEMQUANTITY");


           tableModel.addRow(new Object[]{retrievedItemNum, retrievedItemName, retrievedItemDesc, retrievedItemQuantity});
        }

        if (tableModel.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No matching record found");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Search completed");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }

    } catch (SQLException | NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Incorrect Input or Database Error!");
    }  
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

