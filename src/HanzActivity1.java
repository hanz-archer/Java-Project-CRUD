    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
 
        try {
        con = DriverManager.getConnection(host, uName, uPass);
        stmt = con.createStatement();


        int itemnum = Integer.parseInt(jTextField1.getText());
        String itemname = jTextField2.getText();
        String itemdesc = jTextField3.getText();
        int itemquantity = Integer.parseInt(jTextField4.getText());

        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();

   
        int rowCount = tableModel.getRowCount();
        int rowIndex = -1;

        for (int i = 0; i < rowCount; i++) {
            if (Integer.parseInt(tableModel.getValueAt(i, 0).toString()) == itemnum) {
                rowIndex = i;
                break;
            }
        }

        if (rowIndex != -1) {
      
            tableModel.setValueAt(itemnum, rowIndex, 0);
            tableModel.setValueAt(itemname, rowIndex, 1);
            tableModel.setValueAt(itemdesc, rowIndex, 2);
            tableModel.setValueAt(itemquantity, rowIndex, 3);

     
            String SQL = "UPDATE HANZ.HANZTABLE SET itemnum = " + itemnum + ", itemname = '" + itemname +
                         "', itemdescrip = '" + itemdesc + "', itemquantity = " + itemquantity +
                         " WHERE itemnum = " + itemnum;

            stmt.executeUpdate(SQL);

            JOptionPane.showMessageDialog(this, "Data updated successfully");
        } else {
            JOptionPane.showMessageDialog(this, "Item not found in the current search result");
        }

    } catch (SQLException | NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error updating data");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

